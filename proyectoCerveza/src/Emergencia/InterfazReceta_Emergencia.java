    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed
        // TODO add your handling code here:
        Receta recetaNueva = new Receta();
        recetaNueva.setId_receta(Integer.parseInt(txtNumero.getText()));
        recetaNueva.setRec_cantidad(txtCantidad.getText());
        
        
        Cerveza cerveza = (Cerveza) operacionesCRUD.opBuscarObjeto("Cerveza", cmbCerveza.getSelectedItem().toString());
        Grano grano = (Grano)operacionesCRUD.opBuscarObjeto("Grano", cmbGrano.getSelectedItem().toString());
        
        recetaNueva.formRec_cer(cerveza);
        recetaNueva.formRec_gra(grano);
        
        operacionesCRUD.opPersistObjeto("Receta", recetaNueva);
        actualizarTabla();
        actualizarIDMax();
    }//GEN-LAST:event_btnInsertarActionPerformed

