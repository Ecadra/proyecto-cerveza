    private void actualizarIDMax(){
        txtNumero.setText((operacionesCRUD.opMaxID("Receta")+1)+"");
    }   
    private void actualizarTabla(){
        
        //ACTUALIZAR CUANDO SE IMPLEMENTE LA BUSQUEDA DENTRO DE LA TABLA
        tblRegistros.setModel(operacionesCRUD.opBuscar("Receta", "", ""));
    }
    private void cargarCervezas(){
        List<Cerveza> listaCervezas = operacionesCRUD.opReadObjetos("Cerveza", "", "");
        for(Cerveza cerveza : listaCervezas){
            cmbCerveza.addItem(cerveza.getId_cerveza()+"");
        }
    }
    private void cargarGranos(){
        List<Grano> listaGranos = operacionesCRUD.opReadObjetos("Grano", "", "");
        for(Grano cerveza : listaGranos){
            cmbGrano.addItem(cerveza.getGra_nombre()+"");
        }
    }
    
