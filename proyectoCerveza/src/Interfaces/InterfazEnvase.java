    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        if (validarDatos()) {
            try {
                // Obtener el valor ingresado como capacidad
                int capacidad = Integer.parseInt(txtCapacidad.getText());

                // Verificar si el valor de capacidad excede el l칤mite de un short
                if (capacidad > Short.MAX_VALUE || capacidad < Short.MIN_VALUE) {
                    JOptionPane.showMessageDialog(null, "La capacidad introducida excede el l칤mite de cualquier envase. Intente de nuevo.",
                            "Error de capacidad",
                            JOptionPane.ERROR_MESSAGE);
                } else {
                    // Continuar si la capacidad es v치lida
                    int id = operacionesCRUD.opMaxID("Envase") + 1;
                    Envase nuevoEnvase = new Envase(id, txtTipoEnvase.getText(), (short) capacidad);

                    operacionesCRUD.opPersistObjeto("Envase", nuevoEnvase);
                    System.out.println("Envase registrado exitosamente.");
                }

            } catch (NumberFormatException err) {
                err.printStackTrace(); // Muestra el error completo en la consola para diagn칩stico
                JOptionPane.showMessageDialog(null, "Los datos introducidos no son v치lidos",
                        "Error al registrar el envase",
                        JOptionPane.ERROR_MESSAGE);
            }

        } else {
            mensajeAdvertencia("Registro no realizado", "Error de captura de datos");
        }

        limpiarEnvase();
        activarEnvase(false);

    }//GEN-LAST:event_btnRegistrarActionPerformed

